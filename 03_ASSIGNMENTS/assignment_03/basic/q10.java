package assignment_03.basic;

public class q10 {
    // Perimeter Of Parallelogram
    public static void main(String[] args) {
        double base = 10.0; // Change this value to calculate the perimeter for a different base
        double side = 5.0; // Change this value to calculate the perimeter for a different side length
        double perimeter = 2 * (base + side);
        System.out.println("The perimeter of the parallelogram with base " + base + " and side length " + side + " is: " + perimeter);
    }
}
