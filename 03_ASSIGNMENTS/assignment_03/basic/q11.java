package assignment_03.basic;

public class q11 {
    // perimeter Of Rectangle
    public static void main(String[] args) {
        double length = 10.0; // Change this value to calculate the perimeter for a different length
        double width = 5.0; // Change this value to calculate the perimeter for a different width
        double perimeter = 2 * (length + width);
        System.out.println("The perimeter of the rectangle with length " + length + " and width " + width + " is: " + perimeter);
    }
}
